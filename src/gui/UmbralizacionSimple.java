	public void actualizar(){
		Runnable r = new Runnable() {
			@Override
			public void run() {
				while(true){
					switch(jComboBox1.getSelectedIndex()){
						case 1:{
							jPanel1.setVisible(false);
							jSliderX1.setMinimum(0);
							while(jComboBox1.getSelectedIndex() == 1){
								int x = jSliderX1.getValue();
								imagenResultante = FiltroEspacial.umbralizacionSimple(jSliderX1.getValue(),FiltroEspacial.generaEscalaGrises(imagen));
								jLabel1.setIcon(new ImageIcon(imagenResultante));
							}
							break;
						}
						case 2:{
							jPanel1.setVisible(true);
							jSliderX1.setMinimum(0);
							int x = jSliderX1.getValue();
							imagenResultante = FiltroEspacial.umbralizacionSimple(jSliderX1.getValue(),jSliderX2.getValue(),imagen);
							jLabel1.setIcon(new ImageIcon(imagenResultante));
							break;
						}
						case 3:{
							jPanel1.setVisible(false);
							int x = jSliderX1.getValue();
							jSliderX1.setMinimum(-255);
							imagenResultante = FiltroEspacial.iluminarImagen(imagen, jSliderX1.getValue());
							jLabel1.setIcon(new ImageIcon(imagenResultante));
							break;
						}
					}
				}
			}
		};
		Thread t = new Thread(r);
		t.start();
	}
	
