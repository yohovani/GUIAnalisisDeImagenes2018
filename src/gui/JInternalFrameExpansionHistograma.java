	public void actualizar(){
		Runnable r = new Runnable() {
			@Override
			public void run() {
				while(true){
					
					switch(jComboBox1.getSelectedIndex()){
						case 0:{
							imagenResultante = imagen;
							jLabelImage.setIcon(new ImageIcon(imagenResultante));
							break;
						}
						case 1:{
							imagenResultante = ExpansionHistograma.expansionLn(imagen);
							jLabelImage.setIcon(new ImageIcon(imagenResultante));
							break;
						}
						case 2:{
							int R1=1,R2=255;
							if(jTextFieldR1.getText().matches("[0-9]+") && jTextFieldR2.getText().matches("[0-9]+")){
								R1 = Integer.parseInt(jTextFieldR1.getText());
								R2 = Integer.parseInt(jTextFieldR2.getText());
								if(R1 < 0){
									R1 = 0;
								}
								if(R2 > 255){
									R2 = 255;
								}
							}					
							imagenResultante = ExpansionHistograma.expansionLineal(R1,R2,imagen);
							jLabelImage.setIcon(new ImageIcon(imagenResultante));
							break;
						}
						case 3:{
							jPanel4.setVisible(true);
							double z=0;
							if(!jTextFieldZ.getText().equals("")){
								z = Double.parseDouble(jTextFieldZ.getText());
							}
							imagenResultante = ExpansionHistograma.expansionExp(imagen,z);
							jLabelImage.setIcon(new ImageIcon(imagenResultante));
							break;
						}
					}
				}
			}
		};
		Thread t = new Thread(r);
		t.start();
	}

